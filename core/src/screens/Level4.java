		playArrHeight = PLAY_AR_HEI;
		isFalling = false;
		nextSound = false;
		nextLevelSuspend = 0; //2 secs
		targetRotation = 0;
		timeRemaining = 10; //Secs
        AssetLoader.font1.setColor(Color.WHITE);
        if(lvsPlayAreaRatio < 1.5f)
        	AssetLoader.font1.setScale(0.5f, 0.5f);
        else
        	AssetLoader.font1.setScale(1, 1f);
  
		Add1();
		Add2();
