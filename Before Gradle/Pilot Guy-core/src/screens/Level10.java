		borderLine = new boolean[PLAY_AR_HEI][PLAY_AR_WID];
		playArrWid = PLAY_AR_WID;
		playArrHeight = PLAY_AR_HEI;
		isFalling = false;
		nextSound = false;
		nextLevelSuspend = 0; //2 secs
